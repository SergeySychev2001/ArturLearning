let fruit = "apple";

switch (fruit) {
    case "banana":
        console.log("Это банан");
        break;
    case "apple":
        console.log("Это яблоко");
        break;
    case "orange":
        console.log("Это апельсин");
        break;
    default:
        console.log("Неизвестный фрукт");
}